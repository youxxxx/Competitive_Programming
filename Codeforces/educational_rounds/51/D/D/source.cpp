#include<cstdio>

using namespace std;

int main()
{
	long long dp[2][4][2001] = { 0, };
	long long sol = 0;
	long long n, k, i, j, a;

	scanf("%I64d %I64d", &n, &k);

	dp[0][0][1] = dp[0][3][1] = 1;
	dp[0][1][2] = dp[0][2][2] = 1;

	for (i = 1; i < n; i++)
	{
		dp[i % 2][0][1] = 1;
		dp[i % 2][3][1] = 1;

		dp[i % 2][0][2] = (dp[!(i % 2)][0][2] + dp[!(i % 2)][1][2] + dp[!(i % 2)][2][2] + dp[!(i % 2)][3][1]) % 998244353;
		dp[i % 2][1][2] = (dp[!(i % 2)][0][1] + dp[!(i % 2)][1][2] + dp[!(i % 2)][3][1]) % 998244353;
		dp[i % 2][2][2] = (dp[!(i % 2)][0][1] + dp[!(i % 2)][2][2] + dp[!(i % 2)][3][1]) % 998244353;
		dp[i % 2][3][2] = (dp[!(i % 2)][0][1] + dp[!(i % 2)][1][2] + dp[!(i % 2)][2][2] + dp[!(i % 2)][3][2]) % 998244353;

		for (j = 3; j <= k; j++)
		{
			dp[i % 2][0][j] = (dp[!(i % 2)][0][j] + dp[!(i % 2)][1][j] + dp[!(i % 2)][2][j] + dp[!(i % 2)][3][j - 1]) % 998244353;
			dp[i % 2][1][j] = (dp[!(i % 2)][0][j - 1] + dp[!(i % 2)][1][j] + dp[!(i % 2)][2][j - 2] + dp[!(i % 2)][3][j - 1]) % 998244353;
			dp[i % 2][2][j] = (dp[!(i % 2)][0][j - 1] + dp[!(i % 2)][1][j - 2] + dp[!(i % 2)][2][j] + dp[!(i % 2)][3][j - 1]) % 998244353;
			dp[i % 2][3][j] = (dp[!(i % 2)][0][j - 1] + dp[!(i % 2)][1][j] + dp[!(i % 2)][2][j] + dp[!(i % 2)][3][j]) % 998244353;
		}
	}

	for (i = 0; i < 4; i++)
	{
		sol += dp[!(n % 2)][i][k];
	}

	sol %= 998244353;

	printf("%I64d", sol);

	return 0;
}